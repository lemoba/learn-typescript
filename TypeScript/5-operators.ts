// 1. 算数运算符 + - * / % ++ --
var num1: number = 10;
var num2: number = 20;

console.log(num1 + num2); // 30
console.log(num1 - num2); // -10
console.log(num1 * num2); // 200
console.log(num1 / num2); // 0.5
console.log(num1 % num2); // 10
console.log(++num1); // 11
console.log(--num2); // 19

// 2. 逻辑运算符 == != > < >= <=

if (num1 == num2) {
    console.log("num1 等于 num2");
} else if (num1 <= num2) {
    console.log("num1 小于等于 num2");
} else if (num1 >= num2) {
    console.log("num1 大于等于 num2");
}

